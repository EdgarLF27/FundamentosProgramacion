'''
Programa que solicita 2 puntos y calcula la distancia entre los mismos
Entrada -> distancia 1, distancia 2
Salida -> "La distancia entre dos puntos es: "
'''
n1 = float(input("Ingresa la distancia 1"))
n2 = float(input("Ingresa la distancia 2"))
dist = n1 - n2
print("La distancia entre dos puntos es: ", dist)
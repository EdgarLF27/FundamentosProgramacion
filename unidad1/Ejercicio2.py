'''
Programa que pide base y altura del rectangulo e imprime el area del mismo
Entrada -> base, altura
Salida -> "El area del rectangulo es: area"
'''
base = float(input("Ingresa la base del rectangulo "))
altura =float(input ("Ingresa la altura del rectangulo "))
area = base * altura
print("El area del rectangulo es: " + str(area))